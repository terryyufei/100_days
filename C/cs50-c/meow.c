#include <stdio.h>

/*print meow three times using while loop*/
int main(void)
{
    int i = 0;

    while (i < 3)
    {
        printf("meow\n");
        i++;
    }

}

/*print meow three times using for loop*/

int main(void)
{
    int i;
    for (i = 0; i < 3; i++)
    {
        printf("meow\n");
    }
}